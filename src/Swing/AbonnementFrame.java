    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (idSelectionne <= 0) {
            JOptionPane.showMessageDialog(this, "Sélectionnez un abonnement !");
            return;
        }
        
        int reponse = JOptionPane.showConfirmDialog(this, "Supprimer cet abonnement ?");
        if (reponse == 0) {
            try {
                abonnementService.delete(idSelectionne);
                load();
                clearForm();
                idSelectionne = -1;
                JOptionPane.showMessageDialog(this, "Abonnement supprimé !");
                return;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erreur: " + e.getMessage());
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

     if (idSelectionne <= 0) {
        JOptionPane.showMessageDialog(this, "Sélectionnez un abonnement !");
        return;
    }

    int reponse = JOptionPane.showConfirmDialog(this, "Modifier cet abonnement ?");
    if (reponse != 0) return;

    try {
        String type = getTypeSelectionne();
        if (type.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sélectionnez un type !");
            return;
        }

        String prixStr = jTextField2.getText().trim();
        if (prixStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Le prix est obligatoire !");
            return;
        }
        double prix = Double.parseDouble(prixStr);

        String dureeStr = jTextField1.getText().trim();
        if (dureeStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "La durée est obligatoire !");
            return;
        }
        int duree = Integer.parseInt(dureeStr);

        Abonnement a = new Abonnement(idSelectionne, type, duree, prix);
        abonnementService.update(a);

        load();
        clearForm();
        JOptionPane.showMessageDialog(this, "Abonnement modifié !");
        return;

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Veuillez entrer des nombres valides !");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur: " + e.getMessage());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

