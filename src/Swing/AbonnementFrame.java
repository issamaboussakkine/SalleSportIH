    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        idSelectionne = (int) tableModel.getValueAt(row, 0);
        
        String type = tableModel.getValueAt(row, 1).toString();
        if (type.equalsIgnoreCase("Mensuel")) jRadioButton4.setSelected(true);
        if (type.equalsIgnoreCase("Trimestriel")) jRadioButton5.setSelected(true);
        if (type.equalsIgnoreCase("Annuel")) jRadioButton6.setSelected(true);
        
        String prixStr = tableModel.getValueAt(row, 2).toString().replace(" DH", "");
        jTextField2.setText(prixStr);
        
        String dureeStr = tableModel.getValueAt(row, 3).toString().replace(" jours", "");
        jTextField1.setText(dureeStr);        // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MouseClicked

