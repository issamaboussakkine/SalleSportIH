    void load() {
        tableModel.setRowCount(0);
        try {
            for (Abonnement a : abonnementService.findAll()) {
                tableModel.addRow(new Object[]{
                    a.getId(),
                    a.getType(),
                    a.getPrix() + " DH",
                    a.getDuree() + " jours"
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur: " + e.getMessage());
        }
    }
    
