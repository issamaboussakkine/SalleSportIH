    private void filtrerParType() {
    tableModel.setRowCount(0);
    try {
        List<Abonnement> tous = abonnementService.findAll();
        for (Abonnement a : tous) {
            boolean afficher = true;
            
            if (jRadioButton1.isSelected()) {
                afficher = a.getType().equalsIgnoreCase("Mensuel");
            } else if (jRadioButton2.isSelected()) {
                afficher = a.getType().equalsIgnoreCase("Trimestriel");
            } else if (jRadioButton3.isSelected()) {
                afficher = a.getType().equalsIgnoreCase("Annuel");
            }
            
            if (afficher) {
                tableModel.addRow(new Object[]{
                    a.getId(),
                    a.getType(),
                    a.getPrix() + " DH",
                    a.getDuree() + " jours"
                });
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur: " + e.getMessage());
    }
}
