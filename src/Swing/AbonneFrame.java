    void load() {
    tableModel.setRowCount(0);
    try {
        for (Abonne a : abonneService.findAll()) {
            tableModel.addRow(new Object[]{
                a.getId(),
                a.getNom(),
                a.getAge(),
                a.getSexe()
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur: " + e.getMessage());
    }
    
    
}
    
    
 private void filtrerParAge() {
    int min = 0, max = 0;
    
    if (jRadioButton7.isSelected()) {
        load();  // Recharger tout
        return;
    } else if (jRadioButton3.isSelected()) {
        min = 14; max = 25;
    } else if (jRadioButton4.isSelected()) {
        min = 26; max = 35;
    } else if (jRadioButton5.isSelected()) {
        min = 36; max = 50;
    } else if (jRadioButton6.isSelected()) {
        min = 51; max = 120;
    }
    
    tableModel.setRowCount(0);
    try {
        List<Abonne> tous = abonneService.findAll();
        for (Abonne a : tous) {
            if (a.getAge() >= min && a.getAge() <= max) {
                tableModel.addRow(new Object[]{
                    a.getId(),
                    a.getNom(),
                    a.getAge(),
                    a.getSexe()
                });
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur: " + e.getMessage());
    }
}   
    
