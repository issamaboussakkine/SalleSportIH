    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    String nom = jTextField1.getText();
    int age = Integer.parseInt(jComboBox1.getSelectedItem().toString());
    String sexe = jRadioButton1.isSelected() ? "M" : "F";
    
    int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment modifier cet abonné ?");
    if(reponse == 0){
        Abonne newAbonne = new Abonne(idSelectionne, nom, age, sexe);
        try {
            abonneService.update(newAbonne);
        } catch (Exception ex) {
            Logger.getLogger(AbonneFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        load();
        clearForm();
    }       
    }//GEN-LAST:event_jButton3ActionPerformed

